#include "libft.h"

/**
 * Get the last element of a linked list
 * @param[in] *lst		The list to get the last element of
 * @return			The last element, NULL if the list is empty
 */
t_list	*ft_lstlast(t_list *lst)
{
	if (!lst)
		return (NULL);
	while (lst->next)
		lst = lst->next;
	return (lst);
}
