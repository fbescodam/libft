#include "libft.h"

/**
 * Get the size of a linked list
 * @param[in] *lst		The linked list to get the size of
 * @return			The size of the linked list
 */
int	ft_lstsize(t_list *lst)
{
	int	size;

	size = 0;
	while (lst)
	{
		lst = lst->next;
		size++;
	}
	return (size);
}
