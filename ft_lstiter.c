#include "libft.h"

/**
 * Apply a method to every element of a linked list
 * @param[in] *lst					The list to apply the method to
 * @param[in] void (*f)(void *)		The method to apply to each element
 */
void	ft_lstiter(t_list *lst, void (*f)(void *))
{
	while (lst)
	{
		(*f)(lst->content);
		lst = lst->next;
	}
}
